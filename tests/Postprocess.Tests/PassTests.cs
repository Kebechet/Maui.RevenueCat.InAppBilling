    // ──────────────────────── _Swift_NNNN merging ────────────────────────

    [Fact]
    public void Merge_SingleSuffixIntoCanonical()
    {
        const string input = """
            namespace T
            {
                [BaseType (typeof(NSObject))]
                interface RCAttribution
                {
                    [Export ("enable")]
                    void Enable ();
                }

                [Category]
                [BaseType (typeof(RCAttribution))]
                interface RCAttribution_RevenueCat_Swift_3714
                {
                    [Export ("collect")]
                    void Collect ();
                }
            }
            """;
        var output = ScriptRunner.Run(input);
        Assert.DoesNotContain("_RevenueCat_Swift_3714", output);
        // Canonical should contain BOTH members.
        var canonicalBody = ExtractInterfaceBody(output, "RCAttribution");
        Assert.NotNull(canonicalBody);
        Assert.Contains("void Enable ()", canonicalBody);
        Assert.Contains("void Collect ()", canonicalBody);
    }

    /// <summary>
    /// Regression for the bug where two suffix interfaces targeting the same
    /// canonical caused the merge pass to issue two overlapping replace edits
    /// at the same offset. Applying them in sequence sliced into already-modified
    /// text, dropping members and corrupting comment prefixes (e.g. `//` → `/`).
    /// </summary>
    [Fact]
    public void Regression_Merge_MultipleSuffixesIntoSameCanonical()
    {
        const string input = """
            namespace T
            {
                [BaseType (typeof(NSObject))]
                interface RCAttribution
                {
                    [Export ("enable")]
                    void Enable ();
                }

                [Category]
                [BaseType (typeof(RCAttribution))]
                interface RCAttribution_RevenueCat_Swift_3714
                {
                    // -(void)collect;
                    [Export ("collect")]
                    void Collect ();
                }

                [Category]
                [BaseType (typeof(RCAttribution))]
                interface RCAttribution_RevenueCat_Swift_4901
                {
                    // -(void)setEmail:(NSString *)email;
                    [Export ("setEmail:")]
                    void SetEmail (string email);
                }
            }
            """;
        var output = ScriptRunner.Run(input);
        Assert.DoesNotContain("_RevenueCat_Swift_", output);

        var body = ExtractInterfaceBody(output, "RCAttribution");
        Assert.NotNull(body);
        Assert.Contains("void Enable ()", body);
        Assert.Contains("void Collect ()", body);
        Assert.Contains("void SetEmail (string email)", body);

        // No `//` should have been mangled to `/` (regression).
        Assert.DoesNotMatch(@"^\s*/ -\(", output.ReplaceLineEndings("\n"));
    }

    /// <summary>
    /// Regression for the bug where merging dropped the leading whitespace
    /// before `{` and `}`, leaving braces at column 0 instead of the canonical's
    /// original indent.
    /// </summary>
    [Fact]
    public void Regression_Merge_PreservesBraceIndentation()
    {
        const string input = """
            namespace T
            {
            	[BaseType (typeof(NSObject))]
            	interface RCAttribution
            	{
            		[Export ("enable")]
            		void Enable ();
            	}

            	[Category]
            	[BaseType (typeof(RCAttribution))]
            	interface RCAttribution_RevenueCat_Swift_3714
            	{
            		[Export ("collect")]
            		void Collect ();
            	}
            }
            """;
        var output = ScriptRunner.Run(input);

        // The canonical's `{` and `}` should remain tab-indented (one tab,
        // matching the input), NOT slammed against column 0.
        Assert.Matches(@"interface RCAttribution\s*\r?\n\t\{", output);
        Assert.Matches(@"\r?\n\t\}", output);
    }

