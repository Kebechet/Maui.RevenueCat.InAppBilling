    // ──────────────────────── Multi-line // doc comments ─────────────────

    /// <summary>
    /// Regression for sharpie emitting `// @property ... deprecated("` with the
    /// deprecation message spanning multiple physical newlines. Only the first
    /// line has `//`, so subsequent lines parse as C# code and break the build.
    /// </summary>
    [Fact]
    public void Regression_MultilineDocComment_IsCollapsedOntoOneLine()
    {
        const string input = """
            namespace T
            {
                [BaseType (typeof(NSObject))]
                interface RCFoo
                {
                    // @property (nonatomic) BOOL allowSharing __attribute__((deprecated("
                        Configure behavior through the dashboard instead. If you have configured the "Legacy"
                        restore behavior, keep this active.
                        ")));
                    [Export ("allowSharing")]
                    bool AllowSharing { get; set; }
                }
            }
            """;
        var output = ScriptRunner.Run(input);
        // The collapsed comment line should now contain the entire deprecation
        // message on a single line, and no orphan continuation lines should remain.
        Assert.DoesNotMatch(@"^[ \t]+Configure behavior", output);
        Assert.DoesNotMatch(@"^[ \t]+restore behavior", output);
        Assert.DoesNotMatch(@"^[ \t]+""\)\)\);", output);
        // The property declaration should be intact.
        Assert.Contains("bool AllowSharing { get; set; }", output);
    }

