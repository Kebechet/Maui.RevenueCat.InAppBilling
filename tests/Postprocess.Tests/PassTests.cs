    /// <summary>
    /// Regression for the bug where merging dropped the leading whitespace
    /// before `{` and `}`, leaving braces at column 0 instead of the canonical's
    /// original indent.
    /// </summary>
    [Fact]
    public void Regression_Merge_PreservesBraceIndentation()
    {
        const string input = """
            namespace T
            {
            	[BaseType (typeof(NSObject))]
            	interface RCAttribution
            	{
            		[Export ("enable")]
            		void Enable ();
            	}

            	[Category]
            	[BaseType (typeof(RCAttribution))]
            	interface RCAttribution_RevenueCat_Swift_3714
            	{
            		[Export ("collect")]
            		void Collect ();
            	}
            }
            """;
        var output = ScriptRunner.Run(input);

        // After namespace dedent, the canonical's `{` and `}` should sit at
        // column 0 (the level they originally lived at, minus one for the
        // file-scoped-namespace conversion). The regression we're guarding
        // against is the merge stripping the indent of `{` while leaving the
        // `}` ALSO at column 0 — i.e., both braces should land at the SAME
        // column, paired with the interface declaration's column.
        var lines = output.ReplaceLineEndings("\n").Split('\n');
        var declIdx = Array.FindIndex(lines, l => l.Contains("interface RCAttribution") && !l.Contains("//"));
        Assert.True(declIdx >= 0, "interface declaration not found");
        // Brace lines: the very next line should be `{`, the matching `}` should
        // share its leading whitespace.
        var openLine = lines[declIdx + 1];
        Assert.Matches(@"^\s*\{\s*$", openLine);
        var openIndent = openLine[..(openLine.Length - openLine.TrimStart().Length)];
        // Find the closing `}` for this interface (paired by indentation).
        var closeIdx = Array.FindIndex(lines, declIdx + 1,
            l => l == openIndent + "}");
        Assert.True(closeIdx > declIdx, "matching close brace not found at same indent as open");
    }

