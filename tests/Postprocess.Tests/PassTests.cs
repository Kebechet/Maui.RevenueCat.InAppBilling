    // ──────────────────────── helpers ────────────────────────────────────

    /// <summary>
    /// Returns the inner body text of <c>interface NAME { ... }</c> in <paramref name="source"/>,
    /// or null if not found. Brace nesting one level deep is supported (for property
    /// accessor lists like <c>{ get; }</c>).
    /// </summary>
    private static string? ExtractInterfaceBody(string source, string name)
    {
        var marker = $"interface {name}";
        var idx = source.IndexOf(marker);
        if (idx < 0) return null;
        var brace = source.IndexOf('{', idx + marker.Length);
        if (brace < 0) return null;

        var depth = 1;
        for (var i = brace + 1; i < source.Length; i++)
        {
            if (source[i] == '{') depth++;
            else if (source[i] == '}')
            {
                depth--;
                if (depth == 0) return source[(brace + 1)..i];
            }
        }
        return null;
    }
}

internal static class Substrings
{
    public static int Count(string haystack, string needle)
    {
        if (needle.Length == 0) return 0;
        var count = 0;
        var i = 0;
        while ((i = haystack.IndexOf(needle, i, StringComparison.Ordinal)) >= 0)
        {
            count++;
            i += needle.Length;
        }
        return count;
    }
