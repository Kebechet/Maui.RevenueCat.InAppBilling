using Xunit;

namespace Postprocess.Tests;

/// <summary>
/// End-to-end fixture tests for the post-processor script.
///
/// Each test feeds a self-contained fixture through the script (via
/// <see cref="ScriptRunner"/>) and asserts on the rewritten output. Because
/// each test pays a `dotnet run` cold start, fixtures are kept small and
/// focused; one fixture per behavioral concern rather than long shared inputs.
/// </summary>
public class PassTests
{
    // ──────────────────────── Verify ─────────────────────────────────────

    [Fact]
    public void StripVerify_RemovesStandaloneAttribute()
    {
        const string input = """
            namespace T
            {
                [BaseType (typeof(NSObject))]
                interface RCFoo
                {
                    [Verify (MethodToProperty)]
                    [Export ("bar")]
                    void Bar ();
                }
            }
            """;
        var output = ScriptRunner.Run(input);
        Assert.DoesNotContain("[Verify", output);
        Assert.Contains("[Export (\"bar\")]", output);
        Assert.Contains("void Bar ();", output);
    }

    [Fact]
    public void StripVerify_RemovesInlineAttribute()
    {
        const string input = """
            namespace T
            {
                [BaseType (typeof(NSObject))]
                interface RCFoo
                {
                    [Verify (MethodToProperty)] [Export ("bar")]
                    void Bar ();
                }
            }
            """;
        var output = ScriptRunner.Run(input);
        Assert.DoesNotContain("[Verify", output);
        Assert.Contains("[Export (\"bar\")]", output);
    }

    // ──────────────────────── (AutoGeneratedName = true) ─────────────────

