        Assert.NotNull(body);
        Assert.Contains("void Enable ()", body);
        Assert.Contains("void Collect ()", body);
        Assert.Contains("void SetEmail (string email)", body);

        // No `//` should have been mangled to `/` (regression).
        Assert.DoesNotMatch(@"^\s*/ -\(", output.ReplaceLineEndings("\n"));
